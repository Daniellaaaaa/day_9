# Python Short-hand
first_number=3

first_number += 3 #addition
print(f"The sum is: {first_number}")

second_number=3
second_number -= 3 #subtraction
print(f"The subtraction value is: {second_number}")

first_number *= 3 #multiplication
print(f"The multiplication value is: {first_number}")

first_number /= 3 #division
print(f"The division value is: {first_number}")

first_number //= 3 #floor division
print(f"The floor division value is: {first_number}")

first_number %= 3 #modulus
print(f"The modulus value is: {first_number}")






